    <h2>Ett möte med Ronny Holm</h2>

        <p>I början av 1970-talet började Ronny Holm, att samla de första sakerna, som nu finns i Begravningsmuseet i Ljungby.</p>
        <p>Det var ting som funnits i kyrkor, på kyrkogården, från privatpersoner m fl. Under årens lopp har Ronny Holm handlat i second handbutiker, varit på loppmarknader och besökt auktioner.</p>
        <p>Vid denna tiden var Ronny chef för kyrkogårdsförvalitningen i Ljungby. I samband med en kyrkogårdsvandring arrangerades en utställning med föremål från Ronny Holms samling.</p>
        <p>Flera av deltagarna vid rundvandringen tyckte att utställningen var så värdefull att den borde permanentas. I samband med Alla Helgons Dag, 2004, invigdes så Sveriges första begravningsmuseum som bland annat innehåll material från Ronny Holms privata samling.</p>
        <img src="img/ronny_holm_200.jpg" alt="ronnyh" align:"left">
